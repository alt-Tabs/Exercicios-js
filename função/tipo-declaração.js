// function declaration
function soma (x, y){
    return x+y
}

//function expression (para armazenar funções anonimas em outra função ou constante)
const sub= function(x, y){
    return x-y
}

//named function expression (funções nomeadas atribuida a outra função/const)
const mult = function mult (x, y){
    return x* y
}